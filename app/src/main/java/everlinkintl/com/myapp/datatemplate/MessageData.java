package everlinkintl.com.myapp.datatemplate;

public class MessageData {
    private String time;
    private String messageTitle;
    private String messageSketch;

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getMessageTitle() {
        return messageTitle;
    }

    public void setMessageTitle(String messageTitle) {
        this.messageTitle = messageTitle;
    }

    public String getMessageSketch() {
        return messageSketch;
    }

    public void setMessageSketch(String messageSketch) {
        this.messageSketch = messageSketch;
    }
}
